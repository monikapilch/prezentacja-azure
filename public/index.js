function setImage(imageIndex) {
  const image = images[imageIndex]

  const imageTitle = document.getElementById('image-title')
  imageTitle.innerText = image.title


  const imageSubtitle = document.getElementById('image-subtitle')
  imageSubtitle.innerText = image.subtitle || ''
  imageSubtitle.style.display = image.subtitle ? 'inline' : 'none'

  const imagePreview = document.getElementById('image-preview')
  imagePreview.setAttribute('src', image.path)

  const imageDescription = document.getElementById('image-description')
  imageDescription.innerText = image.description
  imageDescription.style.display = image.description ? 'block' : 'none'
}

setImage(0)

